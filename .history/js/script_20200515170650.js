$(function() {
    $(document).keydown(
        function(e) {
            switch (e.which) {
                case GameSetting.keyPresses.up:
                    console.log("up");
                    break;
                case GameSetting.keyPresses.down:
                    console.log("up");
                    break;
                case GameSetting.keyPresses.left:
                    console.log("up");
                case GameSetting.keyPresses.up:
                    console.log("up");
                    break;
                    break;
                default:
                    break;
            }
        }
    );
});